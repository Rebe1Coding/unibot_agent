"""Tool: think — внутренние размышления агента перед ответом.

Размышление виден пользователю в интерфейсе отдельным блоком (см. streaming.py),
поэтому формулируется понятно и по-русски. Результат вызова в финальный ответ не
попадает — это рабочая фаза рассуждения, а не сам ответ.
"""

from __future__ import annotations

from langchain_core.tools import tool

from app.agent.prompts import THINKING_GUIDELINES


@tool
def think(thought: str) -> str:
    """Обдумать запрос перед ответом.

    Args:
        thought: Рассуждение в свободной форме (2–5 предложений).
    """
    return "Размышление учтено. Примени правила и сформулируй финальный ответ."


# Подробные правила (спецслучаи) держим в описании инструмента, а не в системном
# промпте: они подгружаются из инструкций и не раздувают основной промпт.
think.description = (
    "Инструмент внутреннего размышления. Вызывай его ПЕРВЫМ для каждого "
    "содержательного запроса: разбери, что именно хочет студент, наметь план "
    "действий и обязательно учти правила ниже. Текст размышления показывается "
    "пользователю отдельно от ответа, поэтому пиши понятно и по-русски.\n\n"
    f"{THINKING_GUIDELINES}\n\n"
    "Аргумент thought — твои рассуждения в свободной форме (2–5 предложений)."
)
